package ast;

/**
 * The Type class denotes supported Variable Types.
 * @author Nelson Gou
 * @version 11/16/23
 */
public enum Type
{
    /**
     * The INTEGER Type represents a integer variable.
     */
    INTEGER,
    /**
     * The BOOLEAN Type represents a boolean variable.
     */
    BOOLEAN,
    /**
     * The STRING Type represents a string variable.
     */
    STRING
}
